package com.tx.vo;

public class TimeLineVO {
	/**
	 * 1:天亮了，0:天黑请闭眼
	 */
	private int day;
	
	/**
	 * 被杀者编号
	 */
	private int deadIndex;

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getDeadIndex() {
		return deadIndex;
	}

	public void setDeadIndex(int deadIndex) {
		this.deadIndex = deadIndex;
	}
	
	
}
